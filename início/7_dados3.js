//tipo number, serve para qualquer tipo de número, tanto quebrado quanto inteiro
let idade = 31;
console.log(typeof 31);
console.log(typeof idade);

let salario = 4578.32;
console.log(typeof salario);

//tipo boolean (booleano), que é verdadeiro ou falso (true e false)
let estaChovendo = true; //ou false
console.log(typeof estaChovendo);

//tipo string, texto, pode ser dentro de aspas simples e duplas
console.log(typeof "teste");
console.log(typeof 'teste');